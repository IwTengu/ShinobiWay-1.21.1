package net.iwtengu.shinobiway.combat.clash;

/**
 * ╔══════════════════════════════════════════════════════════════╗
 * ║                        ClashType                            ║
 * ║  Тип столкновения двух ударов.                              ║
 * ╚══════════════════════════════════════════════════════════════╝
 *
 * Определяется в ClashDetector на основе групп оружия обоих игроков.
 *
 * Обозначения:
 *   ARMED  = игрок с оружием (KUNAI, KATANA и любая не-EMPTY группа)
 *   UNARMED = игрок без оружия (EMPTY)
 */
public enum ClashType {

    /**
     * Оружие vs Оружие.
     * Оба игрока с оружием.
     * Результат: урон НЕ наносится, звук клинков, отталкивание.
     */
    ARMED_VS_ARMED,

    /**
     * Кулак vs Кулак.
     * Оба игрока без оружия.
     * Результат: оба получают небольшой урон, звук удара, отталкивание.
     */
    UNARMED_VS_UNARMED,

    /**
     * Кулак vs Оружие (обычное столкновение).
     * Игрок без оружия ударил в окно, но не в идеальное.
     * Результат: безоружный получает урон, вооружённый — место для кастома.
     */
    UNARMED_VS_ARMED,

    /**
     * Кулак vs Оружие (идеальный тайминг — перехват).
     * Безоружный атаковал в perfectClashWindow вооружённого.
     * Результат: безоружный перехватывает оружие вооружённого.
     */
    UNARMED_VS_ARMED_PERFECT
}